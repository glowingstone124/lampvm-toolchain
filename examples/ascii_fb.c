int clear_screen() {
    int *fb = (int *)0x400000;
    int y = 0;
    while (y < 480) {
        int x = 0;
        while (x < 640) {
            fb[y * 640 + x] = COLOR_OFF;
            x = x + 1;
        }
        y = y + 1;
    }
    return 0;
}

int draw_box(int cx, int cy) {
    int *fb = (int *)0x400000;
    int py = cy * 8;
    int px = cx * 8;
    int y = 0;
    while (y < 8) {
        int x = 0;
        while (x < 8) {
            int on = 0;
            if (y == 0 || y == 7 || x == 0 || x == 7) {
                on = 1;
            }
            if (on) {
                fb[(py + y) * 640 + (px + x)] = COLOR_ON;
            } else {
                fb[(py + y) * 640 + (px + x)] = COLOR_OFF;
            }
            x = x + 1;
        }
        y = y + 1;
    }
    return 0;
}

int clear_cell(int cx, int cy) {
    int *fb = (int *)0x400000;
    int py = cy * 8;
    int px = cx * 8;
    int y = 0;
    while (y < 8) {
        int x = 0;
        while (x < 8) {
            fb[(py + y) * 640 + (px + x)] = COLOR_OFF;
            x = x + 1;
        }
        y = y + 1;
    }
    return 0;
}

int draw_char(int ch, int cx, int cy) {
    if (ch < 32 || ch > 126) {
        draw_box(cx, cy);
        return 0;
    }
    int *fb = (int *)0x400000;
    int py = cy * 8;
    int px = cx * 8;
    int row = 0;
    while (row < 8) {
        int col = 0;
        while (col < 8) {
            int v = font[(ch - 32) * 64 + row * 8 + col];
            if (v) {
                fb[(py + row) * 640 + (px + col)] = COLOR_ON;
            } else {
                fb[(py + row) * 640 + (px + col)] = COLOR_OFF;
            }
            col = col + 1;
        }
        row = row + 1;
    }
    return 0;
}

int newline() {
    cursor_x = 0;
    cursor_y = cursor_y + 1;
    if (cursor_y >= ROWS) {
        clear_screen();
        cursor_y = 0;
    }
    return 0;
}

int main() {
    clear_screen();
    while (1) {
        int s = serial_status();
        if (s == 1 || s == 3) {
            int ch = serial_read();
            if (ch == 10) {
                newline();
            } else if (ch == 8) {
                if (cursor_x > 0) {
                    cursor_x = cursor_x - 1;
                } else if (cursor_y > 0) {
                    cursor_y = cursor_y - 1;
                    cursor_x = COLS - 1;
                }
                clear_cell(cursor_x, cursor_y);
            } else if (ch == 13) {
                cursor_x = 0;
            } else {
                draw_char(ch, cursor_x, cursor_y);
                cursor_x = cursor_x + 1;
                if (cursor_x >= COLS) {
                    newline();
                }
            }
        }
    }
    return 0;
}
