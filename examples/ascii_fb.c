int cursor_erase_if_drawn() {
    if (cursor_drawn) {
        redraw_cell_from_buf(cursor_x, cursor_y);
        cursor_drawn = 0;
    }
    return 0;
}

int cursor_draw_if_needed() {
    if (cursor_visible) {
        redraw_cell_from_buf(cursor_x, cursor_y);
        draw_cursor_underline(cursor_x, cursor_y);
        cursor_drawn = 1;
    } else {
        cursor_drawn = 0;
    }
    return 0;
}

int cursor_set(int nx, int ny) {
    cursor_erase_if_drawn();
    cursor_x = nx;
    cursor_y = ny;
    cursor_draw_if_needed();
    return 0;
}

int newline() {
    int nx = 0;
    int ny = cursor_y + 1;
    if (ny >= ROWS) {
        clear_screen();
        int i = 0;
        while (i < COLS * ROWS) {
            screen_buf[i] = 0;
            i = i + 1;
        }
        ny = 0;
    }
    cursor_set(nx, ny);
    return 0;
}
int put_char_at_cursor(int ch) {
    cursor_erase_if_drawn();

    if (ch < 32 || ch > 126) {
        screen_buf[cursor_y * COLS + cursor_x] = 0;
        draw_box(cursor_x, cursor_y);
    } else {
        screen_buf[cursor_y * COLS + cursor_x] = (char)ch;
        draw_char(ch, cursor_x, cursor_y);
    }

    int nx = cursor_x + 1;
    int ny = cursor_y;
    if (nx >= COLS) {
        nx = 0;
        ny = ny + 1;
        if (ny >= ROWS) {
            clear_screen();
            int i = 0;
            while (i < COLS * ROWS) {
                screen_buf[i] = 0;
                i = i + 1;
            }
            ny = 0;
        }
    }
    cursor_x = nx;
    cursor_y = ny;


    cursor_draw_if_needed();
    return 0;
}
int backspace() {
    cursor_erase_if_drawn();

    int nx = cursor_x;
    int ny = cursor_y;

    if (nx > 0) {
        nx = nx - 1;
    } else if (ny > 0) {
        ny = ny - 1;
        nx = COLS - 1;
    }

    cursor_x = nx;
    cursor_y = ny;

    screen_buf[cursor_y * COLS + cursor_x] = 0;
    clear_cell(cursor_x, cursor_y);

    cursor_draw_if_needed();
    return 0;
}
int cursor_blink_tick() {
    u64_pair now;
    u64_pair last;
    u64_pair delta;
    u64_pair period;

    read_from_timer(&now);
    if (!blink_inited) {
        last_blink_lo = now.lo;
        last_blink_hi = now.hi;
        blink_inited = 1;
        return 0;
    }
    last.lo = last_blink_lo;
    last.hi = last_blink_hi;
    u64_pair_sub(&delta, &now, &last);
    period.lo = BLINK_NS_LO;
    period.hi = BLINK_NS_HI;

    if (u64_pair_cmp(&delta, &period) >= 0) {
        last_blink_lo = now.lo;
        last_blink_hi = now.hi;
        cursor_visible = 1 - cursor_visible;

        if (cursor_visible) {
            cursor_draw_if_needed();
        } else {
            cursor_erase_if_drawn();
        }
    }
    return 0;
}
int main() {
    clear_screen();
    int i = 0;
    while (i < COLS * ROWS) {
        screen_buf[i] = 0;
        i = i + 1;
    }
    cursor_visible = 1;
    cursor_draw_if_needed();

    while (1) {
        cursor_blink_tick();

        int s = serial_status();
        if (s == 1 || s == 3) {
            int ch = serial_read();

            if (ch == 10) {
                newline();
            } else if (ch == 8) {
                backspace();
            } else if (ch == 13) {
                cursor_set(0, cursor_y);
            } else {
                put_char_at_cursor(ch);
            }
        }
    }
    return 0;
}
